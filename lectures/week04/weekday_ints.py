'''1-7, Monday-Sunday'''

weekday = int(input("Enter a number 1-7, and I will tell you what day of the week it is: "))

if weekday == 1:
    print("Monday")
elif weekday == 2:
    print("Tuesday")
elif weekday == 3:
    print("Wednesday")
elif weekday == 4:
    print("Thursday")
elif weekday == 5:
    print("Friday")
elif weekday == 6:
    print("Saturday")
elif weekday == 7:
    print("Sunday")
else:
    print("That was not a valid option.")



