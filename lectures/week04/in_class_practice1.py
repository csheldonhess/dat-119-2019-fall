day_num = input("Give me a number, and I'll tell you what day of the week it is: ")

if day_num == "1":
    print("Monday")
elif day_num == "2":
    print("Tuesday")
elif day_num == "3":
    print("Wednesday")
elif day_num == "4":
    print("Thursday")
elif day_num == "5":
    print("Friday")
elif day_num == "6":
    print("Saturday")
elif day_num == "7":
    print("Sunday")
else:
    print("That was not a number corresponding to a day of the week")